import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

const target = path.join(process.cwd(), ".env.local");
const begin = "# >>> vota local supabase";
const end = "# <<< vota local supabase";

function parseEnv(output) {
  const env = {};
  for (const line of output.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const index = trimmed.indexOf("=");
    if (index < 0) continue;
    const key = trimmed.slice(0, index);
    let value = trimmed.slice(index + 1);
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    env[key] = value;
  }
  return env;
}

function statusEnv() {
  try {
    return execFileSync("supabase", ["status", "-o", "env"], {
      cwd: process.cwd(),
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"]
    });
  } catch (error) {
    const stderr = error.stderr ? String(error.stderr) : "";
    throw new Error(
      [
        "Could not read local Supabase status.",
        "Start local Supabase first with: npm run supabase:start",
        stderr.trim()
      ].filter(Boolean).join("\n")
    );
  }
}

function required(env, keys, label) {
  for (const key of keys) {
    if (env[key]) return env[key];
  }
  throw new Error(`Supabase status did not include ${label}. Saw keys: ${Object.keys(env).sort().join(", ")}`);
}

const supabase = parseEnv(statusEnv());
const apiUrl = required(supabase, ["API_URL", "SUPABASE_URL"], "API URL");
const anonKey = required(supabase, ["ANON_KEY", "SUPABASE_ANON_KEY"], "anon key");
const serviceRoleKey = required(supabase, ["SERVICE_ROLE_KEY", "SUPABASE_SERVICE_ROLE_KEY"], "service role key");

const managed = [
  begin,
  "# Generated by `npm run env:local:supabase`. Keep secrets out of source control.",
  "ADMIN_PASSWORD=local-admin-password",
  "ADMIN_SESSION_SECRET=local-admin-session-secret-please-change-for-shared-machines",
  "NEXT_PUBLIC_EVENT_SLUG=megathon-2026",
  "VOTA_DATA_BACKEND=supabase",
  `SUPABASE_URL=${apiUrl}`,
  `SUPABASE_ANON_KEY=${anonKey}`,
  `SUPABASE_SERVICE_ROLE_KEY=${serviceRoleKey}`,
  `NEXT_PUBLIC_SUPABASE_URL=${apiUrl}`,
  `NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}`,
  "NEXT_PUBLIC_BASE_URL=http://127.0.0.1:3000",
  "NEXT_PUBLIC_QR_BASE_URL=http://127.0.0.1:3000",
  "# Empty Mollie key enables the built-in local test checkout route outside production.",
  "MOLLIE_API_KEY=",
  "MOLLIE_READINESS_PAYMENT_ID=",
  "NEXT_PUBLIC_PROOF_REPO_URL=",
  "NEXT_PUBLIC_PROOF_POSTS_URL=",
  "NEXT_PUBLIC_PROOF_DEMO_URL=",
  "NEXT_PUBLIC_PROOF_CHECKOUT_URL=",
  "NEXT_PUBLIC_PROOF_ADMIN_URL=",
  "NEXT_PUBLIC_PROOF_STAGE_URL=",
  end
].join("\n");

const existing = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : "";
const pattern = new RegExp(`${begin}[\\s\\S]*?${end}\\n?`, "m");
const next = pattern.test(existing)
  ? existing.replace(pattern, `${managed}\n`)
  : `${existing.trimEnd()}${existing.trim() ? "\n\n" : ""}${managed}\n`;

fs.writeFileSync(target, next);
console.log(`Wrote local Supabase settings to ${target}`);
